import { Text, TouchableOpacity, TouchableOpacityProps } from "react-native";
import { MaterialIcons } from "@expo/vector-icons";
import { FC, PropsWithChildren } from "react";
import clsx from "clsx";
import { colors } from "@/shared/colors";

type AppButonType = "fill" | "outline";

interface AppButtonParams extends TouchableOpacityProps {
  mode?: AppButonType;
  iconName?: keyof typeof MaterialIcons.glyphMap;
  widthFull?: boolean;
}

export const AppButton: FC<PropsWithChildren<AppButtonParams>> = ({
  children,
  mode = "fill",
  iconName,
  className,
  widthFull = true,
  ...rest
}) => {
  const isFill = mode === "fill";

  return (
    <TouchableOpacity
      {...rest}
      className={clsx(
        widthFull && "w-full",
        className,
        "rounded-xl px-5 flex-row items-center h-button",
        iconName ? "justify-between" : "justify-center",
        {
          "bg-accent-brand": isFill,
          "bg-none border-[1px] border-accent-brand": !isFill,
        }
      )}
    >
      <Text
        className={clsx("text-base", {
          "text-white": isFill,
          "text-accent-brand": !isFill,
        })}
      >
        {children}
      </Text>

      {iconName && (
        <MaterialIcons
          name={iconName}
          size={24}
          color={isFill ? colors.white : colors["accent-brand"]}
        />
      )}
    </TouchableOpacity>
  );
};
